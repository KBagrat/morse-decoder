const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let str = expr;
    let arr = [];

    while (str.length > 0) {
        arr.push(str.slice(0, 10));
        str = str.slice(10);
    }

    return arr.map(el => el.replace(/00/gm, '').replace(/10/gm, ".").replace(/11/gm, "-").replace(/\*{10}/gm, " "))
        .map(el => MORSE_TABLE[el]).map(el => el == undefined ? " " : el).join('');
}

module.exports = {
    decode
};